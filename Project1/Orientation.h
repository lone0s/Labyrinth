/**
 * @file Orientation.h
 * @brief Ce fichier contient la définition de l'énumération Orientation qui représente les 4 orientations possibles.
*/

#pragma once

/**
 * @enum class Orientation
 * @brief Représente les 4 orientations possibles
*/
enum Orientation
{
	/** @brief Haut */
	up = 0,

	/** @brief Droite */
	right = 1,

	/** @brief Bas */
	down = 2,

	/** @brief Gauche */
	left = 3
	
};

